package org.xerp.deliveryservice.models;

import javax.persistence.*;

@Entity
@Table(name = "Point")
public class PointDM {

    @Id
    @GeneratedValue
    private Long id;

    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
